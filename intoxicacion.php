      <h1> Definicion</h1>
<p>Intoxicacion es la reacción del organismo a la entrada de cualquier sustancia tóxica (veneno) que causa lesión o enfermedad y en ocasiones la muerte.
El grado de toxicidad varía según la edad, sexo, estado nutricional, vías de penetración y concentración del tóxico.</p>

                <p>Un Tóxico es cualquier sustancia sólida, líquida o gaseosa que en una concentración determinada puede dañar a los seres vivos. Los tóxicos pueden ser muy variados; los encontramos en plantas, animales, serpientes, peces, insectos, microbios, en gases naturales y artificiales, en sustancias químicas e incluso en medicamentos que según la dosis pueden actuar tóxicamente.</p>



                <p>Teniendo en cuenta la siguiente definicion damos a conocer a nuestros clientes que los productos (piretros) que alfacinco usa en sus servicios  son productos de baja toxicidad, sin embargo siempre recomendamos a los clientes evitar el numero de personas en el area a tratar, para evitar alguna irregularidad en la salud de estas.</p>


