               <p >Las termitas, también conocidas como comején, se alimentan de detritus; consumen plantas y árboles muertos así como las partes muertas de árboles vivos. El aparato bucal de las termitas es capaz de despedazar grandes pedazos de comida, razón por la cual causan daños severos en las viviendas humanas. Aunque las termitas miden solamente un centímetro de largo, su hábitos alimenticios pueden causar daños muy costosos a las propiedades. Las bases de la casa, los muebles, las repisas, los libros, las alfombras y los materiales aislantes, son todos fuentes de comida posibles para las termitas..</p>

<p>Los nidos de las termitas subterráneas en las casas son usualmente formados en el subsuelo. Dentro de estos montículos, las termitas construyen sistemas elaborados de túneles. Adicionalmente construyen túneles verticales de barro a través de los cuales tienen acceso a fuentes de comida sobre la tierra. Las termitas de la madera seca viven dentro de la madera que consumen y suelen infestar paredes y muebles.</p>

<p>Cuando una colonia de termitas está madura, los enjambres de termitas con alas pueden ser vistos alrededor de ventanas y puertas. Las termitas con alas son fuertemente atraídas por las fuentes de luz y son más activas en la primavera. Después del apareamiento, estas termitas encuentran sitios nuevos para anidar y crean nuevas colonias, expandiendo la infestación a múltiples ubicaciones</p>

