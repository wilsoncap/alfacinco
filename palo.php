                <h3>Project Description</h3>
                <p>La paloma es familiar a cualquier persona, tiene el típico cuerpo de color gris, con la rabadilla blanquecina. Presenta dos franjas negras al final de las alas, una franja negra y ancha en la cola y las patas son rojas.</p>

                <p>Las palomas viven en parvadas de varios cientos, que frecuentemente se mueven y perchan juntas, estas subsisten de los desperdicios producto de las actividades humanas, digieren su comida con la molleja, son organismos monógamos, tienen solo una pareja durante toda la vida.</p>

                <p>Sus excretas tienen una alto grado de acido clorhídrico el cual en cantidades debilita los cimientos y las bases metálicas. El excremento de los pájaros ensucia y acelera el deterioro de los edificios, aceras y jardines. Cae sobre automóviles, peatones, empleados y visitantes. También representa riesgos para la salud humana. Está asociado mayormente con alergias y problemas respiratorios</p>

<p>Los nidos de pájaros afean los edificios, tapan las tuberías de desagüe y son reservorios de los parásitos que atacan los pájaros. Ademas, las palomas pueden transmitir enfermedades a los humanos, por eso es tan importante tener un control de palomas eficaz.</p>

